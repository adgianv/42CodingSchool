#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (*s)
	{
		s++;
		i++;
	}
	while (i >= 0)
	{
		if (*s == (char) c)
			return ((char *)s);
		--s;
		--i;
	}
	if (c == '\0')
		return ((char *)s);
	return (0);
}
