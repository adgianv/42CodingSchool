int	ft_iterative_factorial(int nb)
{
	int	i;

	i = nb;
	if (nb > 0)
	{	
		while (i > 1)
		{
			nb *= (i - 1);
			i--;
		}
		return (nb);
	}
	else if (nb == 0)
		return (1);
	else
		return (0);
}
