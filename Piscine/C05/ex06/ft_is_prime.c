int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	while (i <= nb / i)
	{
		if (nb % i == 0 && i != 1 && i != nb)
		{
			break ;
		}
		i++;
	}
	if (nb % i == 0 || nb == 1 || nb == 0)
		return (0);
	else if (nb < 0)
		return (0);
	else
		return (1);
}
